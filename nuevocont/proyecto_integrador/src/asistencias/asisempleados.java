    private int calculoh(String horai, String horaf) {
        String ho = "", mi = "", ho1 = "", mi1 = "";
        int hoc = 0, mic = 0, hoc1 = 0, mic1 = 0;
        for (int i = 0; i < 2; i++) {
            ho = ho + horai.substring(i, i + 1);

            mi = mi + horai.substring(i + 3, i + 4);
            ho1 = ho1 + horaf.substring(i, i + 1);
            mi1 = mi1 + horaf.substring(i + 3, i + 4);
        }
        hoc = Integer.parseInt(ho);
        System.out.println("" + hoc);
        mic = Integer.parseInt(mi);
        System.out.println("" + mic);
        hoc1 = Integer.parseInt(ho1);
        System.out.println("" + hoc1);
        mic1 = Integer.parseInt(mi1);
        System.out.println("" + mic1);
        if (mic1 < mic) {
            mic1 = mic1 + 60;
            mic1 = mic1 - mic;
            hoc = hoc + 1;
        } else {
            mic1 = mic1 - mic;
        }
        hoc1 = hoc1 - hoc;
        hoc1 = hoc1 * 60;

        return (mic1 = mic1 + hoc1);
    }

