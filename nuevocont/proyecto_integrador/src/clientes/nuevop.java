    private void ruc() {
        if (txtruc.getText().length() == 13) {
            boolean veri = validar.validaruc(txtruc.getText());
            boolean veri1 = validar.validarucep(txtruc.getText());
            conectar.conectar();
            int valido = conectar.consultac1get("Select count(*) from proveedores where ruc_proveedor like '" + txtruc.getText() + "'", "count(*)");
            if (veri == true) {
                activa();
            } else if (veri1 == true) {
                activa();
            } else {
                JOptionPane.showMessageDialog(this, "Ruc invalido");
                txtruc.setText("");
                txtruc.requestFocus();
                desactiva();
            }
            if (valido != 1) {
                activa();
            } else {
                JOptionPane.showMessageDialog(this, "Ruc Encontrado en la base de datos");
                desactiva();
            }
        }
        if (txtruc.getText().length() >= 13) {
            txtruc.setText(validar.rucget());
        }
    }
