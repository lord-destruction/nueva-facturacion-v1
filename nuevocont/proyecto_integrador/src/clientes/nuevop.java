    private void btnnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnuevoActionPerformed
        conectar.conectar();
        String consulta = "select * from ciudades where ciudades.nombre_ciudad='" + txtciudad.getText() + "'";
        int indice = conectar.consultac1get(consulta, "cod_ciudad");
        int año = dcfecha.getCalendar().get(Calendar.YEAR);
        int mes = dcfecha.getCalendar().get(Calendar.MONTH) + 1;
        int dia = dcfecha.getCalendar().get(Calendar.DAY_OF_MONTH);
        String consuta = "INSERT INTO `alexanderflower`.`proveedores` (`ruc_proveedor`, `nombre_eproveedor`, `nombres_proveedor`, `apellidos_proveedor`, `direccion_proveedor`, `ciudad_proveedir`, `telefono_proveedor`, `email_proveedor`,`codigofms_proveedor`, `autorizacionsri_proveedor`, `iniciof_proveedor`, `finalizacionf_proveedor`, `fechacf_proveedor`, `estado_proveedor`) VALUES ('" + txtruc.getText() + "', '" + txtempresa.getText() + "', '" + txtnombres.getText() + "', '" + txtapellidos.getText() + "', '" + txtdireccion.getText() + "', " + indice + ", '" + txttelefono.getText() + "', '" + txtemail.getText() + "','" + txtcodigoms.getText() + "', '" + txtautorizacion.getText() + "', '" + txtinicio.getText() + "', '" + txtfinalizacion.getText() + "', '" + año + "-" + mes + "-" + dia + "', 1);";
        conectar.consultaset(consuta);
        conectar.desconectar();
        limpieza();
    }//GEN-LAST:event_btnnuevoActionPerformed
