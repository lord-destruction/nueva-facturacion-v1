    private void txtbusquedaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbusquedaKeyPressed
        conec.conectar();
        final String[] columnas = {"Ruc", "Empresa", "Nombres", "Apellidos", "Direcci√≥n", "Ciudad", "Tel√©fono", "Email"};
        String consulta = "";
        if (rdruc.isSelected()) {
            consulta = "select ruc_proveedor,nombre_eproveedor,nombres_proveedor,apellidos_proveedor,direccion_proveedor,nombre_ciudad,telefono_proveedor,email_proveedor from proveedores,ciudades where proveedores.ciudad_proveedir=ciudades.cod_ciudad and estado_proveedor=1 and ruc_proveedor like '%" + txtbusqueda.getText() + "%';";
        } else if (rdempresa.isSelected()) {
            consulta = "select ruc_proveedor,nombre_eproveedor,nombres_proveedor,apellidos_proveedor,direccion_proveedor,nombre_ciudad,telefono_proveedor,email_proveedor from proveedores,ciudades where proveedores.ciudad_proveedir=ciudades.cod_ciudad and estado_proveedor=1 and nombre_eproveedor like '%" + txtbusqueda.getText() + "%';";
        } else if (rdciudad.isSelected()) {
            consulta = "select * from ciudades where ciudades.nombre_ciudad='" + txtbusqueda.getText() + "'";
            int indice = conec.consultac1get(consulta, "cod_ciudad");
            consulta = "select ruc_proveedor,nombre_eproveedor,nombres_proveedor,apellidos_proveedor,direccion_proveedor,nombre_ciudad,telefono_proveedor,email_proveedor from proveedores,ciudades where proveedores.ciudad_proveedir=ciudades.cod_ciudad and estado_proveedor=1 and ciudad_proveedir like '%" + indice + "%';";
        } else if (rddireccion.isSelected()) {
            consulta = "select ruc_proveedor,nombre_eproveedor,nombres_proveedor,apellidos_proveedor,direccion_proveedor,nombre_ciudad,telefono_proveedor,email_proveedor from proveedores,ciudades where proveedores.ciudad_proveedir=ciudades.cod_ciudad and estado_proveedor=1 and direccion_proveedor like '%" + txtbusqueda.getText() + "%';";
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una opci√≥n");
        }
        tabladatos.setModel(conec.consultaget(consulta, columnas));
        conec.desconectar();
    }//GEN-LAST:event_txtbusquedaKeyPressed

