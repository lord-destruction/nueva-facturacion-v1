    private void btnseleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnseleccionarActionPerformed
        try {
            index = tabladatos.getSelectedRow();
            ruc = tabladatos.getValueAt(index, 0).toString();
            empresa = tabladatos.getValueAt(index, 1).toString();
            nombres = tabladatos.getValueAt(index, 2).toString();
            apellidos = tabladatos.getValueAt(index, 3).toString();
            telefono = tabladatos.getValueAt(index, 6).toString();
            direccion = tabladatos.getValueAt(index, 4).toString();
            ciudad = tabladatos.getValueAt(index, 5).toString();
            email = tabladatos.getValueAt(index, 7).toString();
            conec.conectar();
            String consulta = "select  * from proveedores where ruc_proveedor='" + ruc + "'";
            codigoms = conec.consultac2get(consulta, "codigofms_proveedor");
            autorizacion = conec.consultac2get(consulta, "autorizacionsri_proveedor");
            inicio = conec.consultac2get(consulta, "iniciof_proveedor");
            finalizar = conec.consultac2get(consulta, "finalizacionf_proveedor");
            fechacf = conec.consultac2get(consulta, "fechacf_proveedor");
            conec.desconectar();
            clientesprov panel12 = new clientesprov();
            if (panel12.vermeget() == 3 || panel12.vermeget() == 2) {
                modielip nuevo = new modielip();
                dialogo jd = new dialogo(nuevo, true);
                jd.setContentPane(nuevo.getContentPane());
                jd.pack();
                jd.setLocationRelativeTo(null);
                jd.setVisible(true);

            }
            this.setVisible(false);
        } catch (java.lang.ArrayIndexOutOfBoundsException a) {
            JOptionPane.showMessageDialog(this, "Eliga un registro");
        }

    }//GEN-LAST:event_btnseleccionarActionPerformed

