    private void ruc() {
        if (txtruc.getText().length() == 13) {
            boolean veri = validar.validaruc(txtruc.getText());
            boolean veri1 = validar.validarucep(txtruc.getText());
            if (veri == true) {
                activa();
            } else if (veri1 == true) {
                activa();
            } else {
                JOptionPane.showMessageDialog(this, "Ruc invalido");
                txtruc.setText("");
                txtruc.requestFocus();
                desactiva();
            }
        }
        if (txtruc.getText().length() >= 13) {
            txtruc.setText(validar.rucget());
        }
    }
