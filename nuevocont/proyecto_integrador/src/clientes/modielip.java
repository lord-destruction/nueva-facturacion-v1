    private void cargar() {
        try {
            imagenes p = new imagenes("/proyecto_integrador/imagenes/fondo_gris.jpg");
            this.add(p);
            this.repaint();

            txtruc.setText(buscarp.ruc);
            txtempresa.setText(buscarp.empresa);
            txtnombres.setText(buscarp.nombres);
            txtapellidos.setText(buscarp.apellidos);
            txtdireccion.setText(buscarp.direccion);
            txtemail.setText(buscarp.email);
            txttelefono.setText(buscarp.telefono);
            txtciudad.setText(buscarp.ciudad);
            txtcodigoms.setText(buscarp.codigoms);
            txtautorizacion.setText(buscarp.autorizacion);
            txtinicio.setText(buscarp.inicio);
            txtfinalizacion.setText(buscarp.finalizar);
            SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
            Date nuevaFecha = formatoFecha.parse(buscarp.fechacf);
            dcfecha.setDate(nuevaFecha);
            clientesprov nuevo = new clientesprov();
            if (nuevo.vermeget() == 3) {
                lbltitulo.setText("Modificar Información");
                btneliminar.setVisible(false);
                activa();
                txtruc.setEnabled(false);

            }
            if (nuevo.vermeget() == 2) {

                btnactualizar.setVisible(false);
                btneliminar.setBounds(btnactualizar.location().x, btnactualizar.location().y, btneliminar.getWidth(), btneliminar.getHeight());
                lbltitulo.setText("Eliminar Información");
                desactiva();
            }
            System.out.println("" + nuevo.vermeget());
        } catch (ParseException ex) {
            Logger.getLogger(modielip.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
