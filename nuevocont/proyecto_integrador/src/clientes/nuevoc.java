    private void ruc() {
        if (txtruc.getText().length() == 13) {
            boolean veri = validar.validaruc(txtruc.getText());
            if (veri == true) {
            } else {
                JOptionPane.showMessageDialog(this, "Ruc invalido");
                txtruc.setText("");
                txtruc.requestFocus();
            }
        }
        if (txtruc.getText().length() >= 13) {
            txtruc.setText(validar.rucget());
        }
    }

    private void cedula() {
        if (txtcedula.getText().length() == 10) {
            boolean veri = validar.validarce(txtcedula.getText());
            if (veri == true) {
                activa();

            } else {
                JOptionPane.showMessageDialog(this, "Cédula invalida");
                txtcedula.setText("");
                txtcedula.requestFocus();
                desactiva();
            }
        }
        if (txtcedula.getText().length() >= 10) {
            txtcedula.setText("");
            txtcedula.setText(validar.cedulaget());
        }
    }

