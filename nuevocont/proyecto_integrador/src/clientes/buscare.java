    private void txtbusquedaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbusquedaKeyPressed
        conec.conectar();
        final String[] columnas = {"Cédula", "Nombres", "Apellidos", "Teléfono", "Dirección", "Ciudad", "Móvil", "Email"};
        String consulta = "";
        if (rdcedula.isSelected()) {
            consulta = "SELECT cedula_empleado,nombres_empleado,apellidos_empleado,telefono_empleado,direccion_empleado,nombre_ciudad,movil_empleado,email_empleado FROM empleados,ciudades where ciudades.cod_ciudad=empleados.ciudad_empleado and cedula_empleado like'%" + txtbusqueda.getText() + "%' and estado_empleado=1;";
        } else if (rdnombres.isSelected()) {
            consulta = "SELECT cedula_empleado,nombres_empleado,apellidos_empleado,telefono_empleado,direccion_empleado,nombre_ciudad,movil_empleado,email_empleado FROM empleados,ciudades where ciudades.cod_ciudad=empleados.ciudad_empleado and nombres_empleado like'%" + txtbusqueda.getText() + "%' and estado_empleado=1;";
        } else if (rdapellidos.isSelected()) {
            consulta = "SELECT cedula_empleado,nombres_empleado,apellidos_empleado,telefono_empleado,direccion_empleado,nombre_ciudad,movil_empleado,email_empleado FROM empleados,ciudades where ciudades.cod_ciudad=empleados.ciudad_empleado and apellidos_empleado like'%" + txtbusqueda.getText() + "%' and estado_empleado=1;";
        } else if (rddireccion.isSelected()) {
            consulta = "SELECT cedula_empleado,nombres_empleado,apellidos_empleado,telefono_empleado,direccion_empleado,nombre_ciudad,movil_empleado,email_empleado FROM empleados,ciudades where ciudades.cod_ciudad=empleados.ciudad_empleado and direccion_empleado like'%" + txtbusqueda.getText() + "%' and estado_empleado=1;";
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una opción");
        }
        tabladatos.setModel(conec.consultaget(consulta, columnas));
        conec.desconectar();
    }//GEN-LAST:event_txtbusquedaKeyPressed

