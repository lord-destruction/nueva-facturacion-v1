    private void tabladatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabladatosMouseClicked
        index = tabladatos.getSelectedRow();
        codigo = tabladatos.getValueAt(index, 0).toString();
        descripcion = tabladatos.getValueAt(index, 1).toString();
        tasan = tabladatos.getValueAt(index, 2).toString();
        conec.conectar();
        termino = conec.consultac2get("select * from terminos_pagos where cod_pago=" + codigo + "", "nombre_termino");
        pagoi = conec.consultac2get("select * from terminos_pagos where cod_pago=" + codigo + "", "pago_inicial");
        pagod = conec.consultac2get("select * from terminos_pagos where cod_pago=" + codigo + "", "pago_dias");
        numeroc = conec.consultac2get("select * from terminos_pagos where cod_pago=" + codigo + "", "numero_cuotas");

        conec.desconectar();
        if (tablaopciones.getSelectedIndex() == 1) {
            if (codigo != null) {
                txtdescripcion1.setText(descripcion);
                txttasa1.setText(tasan);
                cbtermino1.setSelectedItem(termino);
                combo();
                txteuno1.setText(pagoi);

                txtetres1.setText(pagod);
                txtdescripcion1.setText(descripcion);
                txtedos1.setText(numeroc);
            }
        } else if (tablaopciones.getSelectedIndex() == 2) {
            if (codigo != null) {
                txtdescripcion2.setText(descripcion);
                txtdescripcion2.setEnabled(false);
                cbtermino2.setSelectedItem(termino);
                cbtermino2.setEnabled(false);
            }
        }
    }//GEN-LAST:event_tabladatosMouseClicked

