        jd.setVisible(true);
        String cedula = buscare.cedula;
        cod = conec.consultac2get("select a.cod_anticipo,a.fecha_anticipo,a.cod_empleado,a.valor_anticipo,a.concepto_anticipo from anticipos a where cod_empleado='" + cedula + "' order by a.fecha_anticipo desc limit 1", "a.cod_anticipo");
        
        txtempleado.setText(conec.consultac2get("select a.cod_anticipo,a.fecha_anticipo,a.cod_empleado,a.valor_anticipo,a.concepto_anticipo from anticipos a where cod_empleado='" + cedula + "' order by a.fecha_anticipo desc limit 1", "a.cod_empleado"));
        txtvalor.setText(conec.consultac2get("select a.cod_anticipo,a.fecha_anticipo,a.cod_empleado,a.valor_anticipo,a.concepto_anticipo from anticipos a where cod_empleado='" + cedula + "' order by a.fecha_anticipo desc limit 1", "a.valor_anticipo"));
        txtconcepto.setText(conec.consultac2get("select a.cod_anticipo,a.fecha_anticipo,a.cod_empleado,a.valor_anticipo,a.concepto_anticipo from anticipos a where cod_empleado='" + cedula + "' order by a.fecha_anticipo  desc limit 1", "concepto_anticipo"));
        conec.desconectar();
        if ("".equals(txtempleado.getText())) {
            JOptionPane.showMessageDialog(this, "No Existe registros");
        }
