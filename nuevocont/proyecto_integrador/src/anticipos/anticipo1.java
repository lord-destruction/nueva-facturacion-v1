    private void btnnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnuevoActionPerformed
        
        if (txtempleado.getText().length() > 0 && txtvalor.getText().length() > 0) {
            conec.conectar();
            int año = dcfecha.getCalendar().get(Calendar.YEAR);
            int mes = dcfecha.getCalendar().get(Calendar.MONTH) + 1;
            int dia = dcfecha.getCalendar().get(Calendar.DAY_OF_MONTH);
            conec.consultaset("INSERT INTO `alexanderflower`.`anticipos` (`fecha_anticipo`, `cod_empleado`, `valor_anticipo`, `concepto_anticipo`) VALUES ('" + año + "-" + mes + "-" + dia + "', '" + txtempleado.getText() + "', '" + txtvalor.getText() + "', '" + txtconcepto.getText() + "');");
            String codigoa = conec.consultac2get("select t.transmanu from transacciones t where transmanu is not null order by transmanu desc limit 1", "transmanu");
            if ("".equals(codigoa)) {
                codigoa = "0";
            }
            int codigom = Integer.parseInt(codigoa);
            codigom++;
            conec.consultaset("INSERT INTO `alexanderflower`.`transacciones` (`fecha_trans`, `cod_cuenta`, `des_cuenta`, `debe`, `reciboco`) VALUES ('" + año + "-" + mes + "-" + dia + "', '" + conec.consultac2get("select * from subcuenta2 where nombre_subcuenta2='BANCOS'", "cod_subcuenta2") + "', 'BANCOS', '" + txtvalor.getText() + "', '" + codigom + "');");
            conec.consultaset("INSERT INTO `alexanderflower`.`transacciones` (`fecha_trans`, `cod_cuenta`, `des_cuenta`, `haber`, `reciboco`) VALUES ('" + año + "-" + mes + "-" + dia + "', '" + conec.consultac2get("select * from subcuenta2 where nombre_subcuenta2='ANTICIPOS QUINCENA'", "cod_subcuenta2") + "', 'ANTICIPOS QUINCENA', " + txtvalor.getText() + ", '" + codigom + "');");
            conec.desconectar();
            txtempleado.setText("");
            txtvalor.setText("");
            txtconcepto.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Llene todos lo campos");
        }
    }//GEN-LAST:event_btnnuevoActionPerformed
