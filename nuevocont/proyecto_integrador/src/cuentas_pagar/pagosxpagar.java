    private void balance() {

        if (!"0.0".equals(tabladatos1.getValueAt(index, 6).toString()) && "0.0".equals(tabladatos1.getValueAt(index, 7).toString()) && "0.0".equals(tabladatos1.getValueAt(index, 8).toString())) {
            balance = tabladatos1.getValueAt(index, 6).toString();
        } else if (!"0.0".equals(tabladatos.getValueAt(index, 6).toString()) && !"0.0".equals(tabladatos1.getValueAt(index, 7).toString()) && "0.0".equals(tabladatos1.getValueAt(index, 8).toString())) {
            balance = tabladatos1.getValueAt(index, 7).toString();
        } else if (!"0.0".equals(tabladatos1.getValueAt(index, 6).toString()) && !"0.0".equals(tabladatos1.getValueAt(index, 7).toString()) && !"0.0".equals(tabladatos1.getValueAt(index, 8).toString())) {
            balance = tabladatos1.getValueAt(index, 8).toString();
        }
    }
