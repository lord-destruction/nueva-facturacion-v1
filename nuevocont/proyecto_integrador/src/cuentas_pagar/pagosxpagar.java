    private void btnpagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpagoActionPerformed
        interes = "0";
        balance = tabladatos.getValueAt(index, 6).toString();
        estado = tabladatos.getValueAt(index, 7).toString();
        numeroc = tabladatos.getValueAt(index, 0).toString();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        try {
            fecha1 = formatoFecha.parse(tabladatos.getValueAt(index, 2).toString());
        } catch (ParseException ex) {
            Logger.getLogger(pagosxpagar.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!"Cerrado".equals(estado) && fecha.before(fecha1)) {
            cierre();
        }
    }//GEN-LAST:event_btnpagoActionPerformed
    private void cierre() {
        int control = 0, otro = 0;
        for (int i = 0; i < tabladatos.getRowCount() - 1; i++) {
            if ("Cerrado".equals(tabladatos.getValueAt(i, 7).toString()) || "Vencido".equals(tabladatos.getValueAt(i, 7).toString())) {
                control = i;
                otro = 1;
            }
        }
        if (control == index) {
            reciboxpagar nu = new reciboxpagar();
            dialogo jd = new dialogo(nu, true);
            jd.setContentPane(nu.getContentPane());
            jd.pack();
            jd.setLocationRelativeTo(null);
            jd.setVisible(true);
            mostrar();
        }
        if (control + 1 == index && otro != 0) {
            reciboxpagar nu = new reciboxpagar();
            dialogo jd = new dialogo(nu, true);
            jd.setContentPane(nu.getContentPane());
            jd.pack();
            jd.setLocationRelativeTo(null);
            jd.setVisible(true);
            mostrar();
        }

    }

    private void cierre1() {
        int control = 0;
        for (int i = 0; i < tabladatos1.getRowCount() - 1; i++) {
            if ("Cerrado".equals(tabladatos1.getValueAt(i, 9).toString())) {
                control = i;
            }
        }
        if (control + 1 == index) {
            reciboxpagar nu = new reciboxpagar();
            dialogo jd = new dialogo(nu, true);
            jd.setContentPane(nu.getContentPane());
            jd.pack();
            jd.setLocationRelativeTo(null);
            jd.setVisible(true);
            mostrar();
        }

    }
