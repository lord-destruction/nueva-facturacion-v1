    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        if (txtproducto.getText().length() > 0) {
            conec.conectar();
            String proveedor = conec.consultac2get("select *  from proveedores where nombre_eproveedor='" + txtproveedor.getText() + "'", "ruc_proveedor");
            int subcategoria = conec.consultac1get("select *  from subcategoria where nombre_subcategoria='" + txtsubcategoria.getText() + "'", "cod_subcategoria");
            int bodega = conec.consultac1get("select *  from bodega where nombre_bodega='" + txtbodega.getText() + "'", "cod_bodega");
            int unidadv = conec.consultac1get("select *  from unidades where nombre_unidad='" + txtunidadv.getText() + "'", "cod_unidad");
            int unidadc = conec.consultac1get("select *  from unidades where nombre_unidad='" + txtunidadc.getText() + "'", "cod_unidad");
            String consulta = "UPDATE `alexanderflower`.`productos` SET `nombre_producto`='" + txtproducto.getText() + "',`unidad_productoc`='" + unidadc + "', `cantidadi_producto`=" + txtcantidadi.getText() + ", `cantidadmi_producto`=" + txtcantidadmi.getText() + ", `cantidadma_producto`=" + txtcantidadma.getText() + ", `proveedor_producto`='" + proveedor + "', `subcategoria_producto`=" + subcategoria + ", `bodega_producto`=" + bodega + ", `unidad_productov`=" + unidadv + ", `cantidadauni_producto`=" + txtcontiene.getText() + ", `precio_producto`=" + txtprecio.getText() + " WHERE `cod_producto`='" + codigo + "';";
            conec.consultaset(consulta);
            conec.desconectar();
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnactualizarActionPerformed
