    private void cargar() {

        txtcantidadi.setEnabled(false);
        codigo = buscarpro.codigo;
        txtproducto.setText(buscarpro.producto);
        txtcantidadi.setText(buscarpro.cantidadi);
        txtcantidadmi.setText(buscarpro.cantidadmi);
        txtcantidadma.setText(buscarpro.cantidadma);
        txtprecio.setText(buscarpro.precio);
        txtproveedor.setText(buscarpro.proveedor);
        txtcategoria.setText(buscarpro.subcategoria);
        txtsubcategoria.setText(buscarpro.subcategoria);
        txtbodega.setText(buscarpro.bodega);
        txtunidadc.setText(buscarpro.unidadc);
        txtunidadv.setText(buscarpro.unidadv);
        txtcontiene.setText(buscarpro.cantidaduni);
        if (panelinventario.vermep == 0) {
            lbltitulo.setText("Eliminar Información");
            btneliminar.setLocation(btnactualizar.getLocation().x, btnactualizar.getLocation().y);
            btnactualizar.setVisible(false);
            eliminar();

        } else if (panelinventario.vermep == 1) {
            lbltitulo.setText("Actualizar Información");
            btneliminar.setVisible(false);
        }
    }
