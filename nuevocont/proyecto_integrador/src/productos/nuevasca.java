    private void tablaopcionesStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tablaopcionesStateChanged
        if (tablaopciones.getSelectedIndex() == 0) {

            if (nuevopro.ca == 0 && nuevasca.bcategoria == 0 && modielipro.ca == 0) {
                txtcategorian.setEnabled(false);
                i = 0;
                limpiar();
            } else {
                btnseleccionar.setEnabled(true);
            }

        }
        if (tablaopciones.getSelectedIndex() == 1) {
            i = 1;
            categoria = "";
            txtsubcategoriam.setEnabled(false);
            txtcategoriam.setEnabled(false);
            limpiar();
        }
        if (tablaopciones.getSelectedIndex() == 2) {
            i = 1;
            categoria = "";
            limpiar();
            txtsubcategoriae.setEnabled(false);
            txtcategoriae.setEnabled(false);
        }
    }//GEN-LAST:event_tablaopcionesStateChanged

