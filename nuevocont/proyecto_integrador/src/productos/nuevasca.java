    private void tabladatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabladatosMouseClicked
        index = tabladatos.getSelectedRow();
        categoria = tabladatos.getValueAt(index, 1).toString();
        codigo = tabladatos.getValueAt(index, 0).toString();
        subcategoria = tabladatos.getValueAt(index, 2).toString();
        if (tablaopciones.getSelectedIndex() == 1) {
            if (subcategoria != null) {
                txtsubcategoriam.setText(categoria);
                txtcategoriam.setText(subcategoria);
                txtsubcategoriam.setEnabled(true);

            }
        } else if (tablaopciones.getSelectedIndex() == 2) {
            if (subcategoria != null) {
                txtsubcategoriae.setText(categoria);
                txtcategoriae.setText(subcategoria);
            }
        }
    }//GEN-LAST:event_tabladatosMouseClicked

    private void txtbusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtbusquedaActionPerformed
        conec.conectar();
        final String[] columnas = {"CÃ³digo", "SubCategoria", "Categoria"};
        String consulta = "";
        if (rdsubcategoria.isSelected()) {
            consulta = "select cod_subcategoria,nombre_subcategoria,nombre_categoria from categorias,subcategoria where categorias.cod_categoria=subcategoria.cod_categoria and estado_subcategoria=1 and subcategoria.nombre_subcategoria like '%" + txtbusqueda.getText() + "%'";
        }
        if (rdcategoria.isSelected()) {
            consulta = "select * from categorias where categorias.nombre_categoria like'%" + txtbusqueda.getText() + "%'";
            int indice = conec.consultac1get(consulta, "cod_categoria");
            consulta = "select cod_subcategoria,nombre_subcategoria,nombre_categoria from categorias,subcategoria where categorias.cod_categoria=subcategoria.cod_categoria and estado_subcategoria=1 and subcategoria.cod_categoria like '%" + indice + "%'";
        }
        tabladatos.setModel(conec.consultaget(consulta, columnas));
        conec.desconectar();
    }//GEN-LAST:event_txtbusquedaActionPerformed

