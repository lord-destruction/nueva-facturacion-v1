    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        if (txtproducto.getText().length() > 0) {
            conec.conectar();
            String proveedor = conec.consultac2get("select *  from proveedores where nombre_eproveedor='" + txtproveedor.getText() + "'", "ruc_proveedor");
            int subcategoria = conec.consultac1get("select *  from subcategoria where nombre_subcategoria='" + txtsubcategoria.getText() + "'", "cod_subcategoria");
            int bodega = conec.consultac1get("select *  from bodega where nombre_bodega='" + txtbodega.getText() + "'", "cod_bodega");
            int unidad = conec.consultac1get("select *  from unidades where nombre_unidad='" + txtunidadc.getText() + "'", "cod_unidad");
            int unidad1 = conec.consultac1get("select *  from unidades where nombre_unidad='" + txtunidadv.getText() + "'", "cod_unidad");
            String consulta = "INSERT INTO `alexanderflower`.`productos` (`nombre_producto`, `cantidadi_producto`, `cantidadmi_producto`, `cantidadma_producto`, `proveedor_producto` ,`subcategoria_producto`, `bodega_producto`, `unidad_productoc`, `unidad_productov`, `cantidadauni_producto`, `precio_producto`, `estado_producto`) VALUES ('" + txtproducto.getText() + "'," + txtcantidadi.getText() + ", " + txtcantidadmi.getText() + ", " + txtcantidadma.getText() + ", " + proveedor + "," + subcategoria + ", " + bodega + ", " + unidad + ", " + unidad1 + ", " + txtcontiene.getText() + ", " + txtprecio.getText() + ", 1);";
            conec.consultaset(consulta);
            conec.desconectar();
            limpieza();
        }
    }//GEN-LAST:event_btnaceptarActionPerformed
