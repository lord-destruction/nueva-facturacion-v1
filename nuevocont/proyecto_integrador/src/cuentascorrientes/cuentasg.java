    private void btnaceptarccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarccActionPerformed
        int a帽o = dcdesde.getCalendar().get(Calendar.YEAR);
        int mes = dcdesde.getCalendar().get(Calendar.MONTH) + 1;
        int dia = dcdesde.getCalendar().get(Calendar.DAY_OF_MONTH);
        for (int i = 0; i < tablacc.getRowCount(); i++) {
            conec.conectar();
            if ("Cobrado".equals(tablacc.getValueAt(i, 6).toString())) {
                conec.consultaset("UPDATE `alexanderflower`.`cheques` SET `pago_cheque`='Cobrado H' WHERE `num_cheque`='" + tablacc.getValueAt(i, 1).toString() + "' and`num_cuenta`='" + listacuentas.ndes + "';");

                double saldoa = Double.parseDouble(conec.consultac2get("select * from cuentascorr where num_cuentacorr='" + listacuentas.ndes + "'", "saldo_cuentacorr"));
                saldoa = saldoa - Double.parseDouble(tablacc.getValueAt(i, 5).toString());
                conec.consultaset("UPDATE `alexanderflower`.`cuentascorr` SET `saldo_cuentacorr`=" + saldoa + " WHERE `num_cuentacorr`='" + listacuentas.ndes + "';");
                String codigoa = conec.consultac2get("select t.transmanu from transacciones t where transmanu is not null order by transmanu desc limit 1", "transmanu");
                if ("".equals(codigoa)) {
                    codigoa = "0";
                }
                int codigom = Integer.parseInt(codigoa);
                codigom++;
                conec.consultaset("INSERT INTO `alexanderflower`.`transacciones` (`fecha_trans`, `cod_cuenta`, `des_cuenta`, `debe`, `reciboco`) VALUES ('" + a帽o + "-" + mes + "-" + dia + "', '" + conec.consultac2get("select * from subcuenta2 where nombre_subcuenta2='BANCOS'", "cod_subcuenta2") + "', 'BANCOS', '" + tablacc.getValueAt(i, 5) + "', '" + codigom + "');");
                conec.consultaset("INSERT INTO `alexanderflower`.`transacciones` (`fecha_trans`, `cod_cuenta`, `des_cuenta`, `haber`, `reciboco`) VALUES ('" + a帽o + "-" + mes + "-" + dia + "', '" + conec.consultac2get("select * from subcuenta2 where nombre_subcuenta2='ANTICIPOS QUINCENA'", "cod_subcuenta2") + "', 'ANTICIPOS QUINCENA', " + tablacc.getValueAt(i, 5) + ", '" + codigom + "');");

                saldoch();
            }
            conec.desconectar();
        }
        chequesc();
        JOptionPane.showMessageDialog(this, "Se Ha realizado con exito");

    }//GEN-LAST:event_btnaceptarccActionPerformed

