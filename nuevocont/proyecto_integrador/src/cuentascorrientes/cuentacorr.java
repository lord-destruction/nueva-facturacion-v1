    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if (!"".equals(txtnumero.getText()) && !"".equals(txtbanco.getText())) {
            conec.conectar();
            conec.consultaset("INSERT INTO `alexanderflower`.`cuentascorr` (`num_cuentacorr`, `cod_banco`, `des_cuentacorr`, `saldo_cuentacorr`, `estado_cuenta`) VALUES ('" + txtnumero.getText() + "', '" + conec.consultac2get("select * from bancos where nombre_banco='" + txtbanco.getText() + "'", "cod_banco") + "', '" + txtdescripcion.getText() + "', '" + txtsaldo.getText() + "', '1');");
            conec.desconectar();
            limpieza();
        }
    }//GEN-LAST:event_btnguardarActionPerformed

