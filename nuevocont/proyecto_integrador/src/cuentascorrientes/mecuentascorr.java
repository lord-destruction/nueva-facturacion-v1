    private void inicio() {
        String consulta = "select * from  cuentascorr where num_cuentacorr='" + cuentasg.ncuenta + "'";
        conec.conectar();
        if (cuentasg.estadocrr == 1) {
            txtnumero.setEnabled(false);
            txtnumero.setText(conec.consultac2get(consulta, "num_cuentacorr"));
            txtcodigo.setText(conec.consultac2get(consulta, "cod_banco"));
            txtbanco.setText(cuentasg.banco);
            txtdescripcion.setText(conec.consultac2get(consulta, "des_cuentacorr"));
            txtsaldo.setText(conec.consultac2get(consulta, "saldo_cuentacorr"));
            txtccodigo.setText(conec.consultac2get(consulta, "cod_cuentacon"));
            txtcuenta.setText(conec.consultac2get(consulta, "nombre_cuenta"));
        } else if (cuentasg.estadocrr == 2) {
            txtnumero.setText(conec.consultac2get(consulta, "num_cuentacorr"));
            txtcodigo.setText(conec.consultac2get(consulta, "cod_banco"));
            txtbanco.setText(cuentasg.banco);
            txtdescripcion.setText(conec.consultac2get(consulta, "des_cuentacorr"));
            txtsaldo.setText(conec.consultac2get(consulta, "saldo_cuentacorr"));
            txtccodigo.setText(conec.consultac2get(consulta, "cod_cuentacon"));
            txtcuenta.setText(conec.consultac2get(consulta, "nombre_cuenta"));
            desactivo();
        }
        conec.desconectar();
    }

