    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if (cuentasg.estadocrr == 1) {
            if (!"".equals(txtnumero.getText()) && !"".equals(txtbanco.getText()) && !"".equals(txtcuenta.getText())) {
                conec.conectar();
                conec.consultaset("UPDATE `alexanderflower`.`cuentascorr` SET `cod_banco`='" + conec.consultac2get("select * from bancos where nombre_banco='" + txtbanco.getText() + "'", "cod_banco") + "', `des_cuentacorr`='" + txtdescripcion.getText() + "', `saldo_cuentacorr`='" + txtsaldo.getText() + "', `cod_cuentacon`='" + txtccodigo.getText() + "', `nombre_cuenta`='" + txtcuenta.getText() + "' WHERE `num_cuentacorr`='" + txtnumero.getText() + "';");
                conec.desconectar();
                limpieza();
            }
        } else if (cuentasg.estadocrr == 2) {
            int seleccion = JOptionPane.showOptionDialog(this, "¿Confimar Borrado", "Borrar", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Si", "No"}, "Si");
            if (seleccion == 0) {
                conec.conectar();
                conec.consultaset("UPDATE `alexanderflower`.`cuentascorr` SET `estado_cuenta`='0' WHERE `num_cuentacorr`='" + txtnumero.getText() + "';");
                conec.desconectar();
                limpieza();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

