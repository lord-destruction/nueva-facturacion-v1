    private void txtbusquedaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbusquedaKeyPressed
        String consulta = "";
        conec.conectar();
        String columnas[] = {"Codigo", "Banco", "Sucursal", "Ciudad"};
        if (rdcodigo.isSelected()) {
            consulta = "select b.cod_banco,b.nombre_banco,b.sucursal_banco,c.nombre_ciudad from bancos b,ciudades c where b.ciudad_banco=c.cod_ciudad and b.cod_banco like '%" + txtbusqueda.getText() + "%' and estado_banco=1";
        } else if (rdbanco.isSelected()) {
            consulta = "select b.cod_banco,b.nombre_banco,b.sucursal_banco,c.nombre_ciudad from bancos b,ciudades c where b.ciudad_banco=c.cod_ciudad and b.nombre_banco like '%" + txtbusqueda.getText() + "%' and estado_banco=1";
        } else if (rdciudad.isSelected()) {
            consulta = "select b.cod_banco,b.nombre_banco,b.sucursal_banco,c.nombre_ciudad from bancos b,ciudades c where b.ciudad_banco=c.cod_ciudad and b.ciudad_banco='" + conec.consultac2get("select * from ciudades where nombre_ciudad='" + txtbusqueda.getText() + "'", "cod_ciudad") + "' and estado_banco=1";
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una Opcion \n de buscqueda");
        }
        tabladatos.setModel(conec.consultaget(consulta, columnas));
    }//GEN-LAST:event_txtbusquedaKeyPressed
