    private int recursivo(String cuentai) {
        String codig = cuentai;
        int puntos = 0;
        for (int i = 0; i < codig.length(); i++) {
            if (".".equals(codig.substring(i, i + 1))) {
                puntos++;
            }
        }
        return puntos;
    }
    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed

        if (!"".equals(txtbanco.getText()) && !"".equals(txtciudad.getText()) && !"".equals(txtdireccion.getText())) {
            conec.conectar();
            conec.consultaset("INSERT INTO `alexanderflower`.`bancos` (`nombre_banco`, `sucursal_banco`, `ciudad_banco`, `direccion_banco`, `telefono_banco`, `email_banco`,`estado_banco`) VALUES ('" + txtbanco.getText() + "', '" + txtextension.getText() + "', '" + conec.consultac2get("select * from ciudades where nombre_ciudad='" + txtciudad.getText() + "'", "cod_ciudad") + "', '" + txtdireccion.getText() + "', '" + txttelefono.getText() + "', '" + txtemail.getText() + "','1');");
            String indice = conec.consultac2get("select * from subcuenta2 where nombre_subcuenta2='BANCOS'", "cod_subcuenta2");
            String cuenta = conec.consultac2get("select cod_subcuenta3 from subcuenta3 where cod_subcuenta2='" + indice + "' order by cod_subcuenta3 desc limit 1", "cod_subcuenta3");
            String aux = "";
            for (int p = cuenta.length(); p > 0; p--) {
                if (!".".equals(cuenta.substring(p - 1, p))) {
                    aux = aux + cuenta.substring(p - 1, p);
                } else {
                    break;
                }

            }
            if ("".equals(aux)) {
                aux = "0";
            }
            int conver = Integer.parseInt(aux);
            conver++;
            String codigo1 = indice + "." + conver;
            conec.consultaset("INSERT INTO `alexanderflower`.`subcuenta3` (`cod_subcuenta3`,`nombre_subcuenta3`, `cod_subcuenta2`, `estado_subcuenta3`) VALUES ('" + codigo1 + "','" + txtbanco.getText() + "', '" + indice + "', 1);");
            conec.desconectar();
            limpieza();
        } else {
            JOptionPane.showMessageDialog(this, "Llene los datos Banco,Ciudad y Dirección");
        }

    }//GEN-LAST:event_btnaceptarActionPerformed

