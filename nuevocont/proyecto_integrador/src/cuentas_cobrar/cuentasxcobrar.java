private void arranque() {
        int año = dcinicio.getCalendar().get(Calendar.YEAR);
        int mes = dcinicio.getCalendar().get(Calendar.MONTH) + 1;
        int dia = dcinicio.getCalendar().get(Calendar.DAY_OF_MONTH);
        int año1 = dchasta.getCalendar().get(Calendar.YEAR);
        int mes1 = dchasta.getCalendar().get(Calendar.MONTH) + 1;
        int dia1 = dchasta.getCalendar().get(Calendar.DAY_OF_MONTH);
        if (txtcodigo.getText().length() == 10) {
            conec.conectar();
            final String[] columnas = {"Codigo", "Cédula", "Cliente", "Ult. Mov", "Ult. Monto", "Balance Total"};
            String consulta = "select f.codigo_fact,f.numero_cliente,nombres_cliente,f.fecha_emision,f.total_facturave, f.total_facturave  from clientes c,facturasve f where c.cedula_cliente=f.numero_cliente  and f.numero_cliente='" + txtcodigo.getText() + "' and f.termino_pago!=1 and(f.fecha_emision>'" + año + "-" + mes + "-" + dia + "' and f.fecha_emision<'" + año1 + "-" + mes1 + "-" + dia1 + "') order by f.fecha_emision desc,f.total_facturave desc";
            tabladatos.setModel(conec.consultaget(consulta, columnas));
            conec.desconectar();
        }

    }
