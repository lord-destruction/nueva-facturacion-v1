    private void tabladatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabladatosMouseClicked
        int index = tabladatos.getSelectedRow();
        codigo = tabladatos.getValueAt(index, 0).toString();
        nombre = tabladatos.getValueAt(index, 1).toString();
        txtclientee.setText(tabladatos.getValueAt(index, 1).toString());
        conec.conectar();
        final String[] columnas = {"Fecha", "Nro. cuota", "Nro. Factura", "Monto"};
        String consulta = "select fecha_cobrar,numero_cuota,numero_factura,monto_cuota from cuentas_cobrar c,facturasve f where f.codigo_fact=c.numero_factura and f.codigo_fact=" + tabladatos.getValueAt(index, 0).toString() + "";
        tablaestado.setModel(conec.consultaget(consulta, columnas));
        conec.desconectar();
    }//GEN-LAST:event_tabladatosMouseClicked

