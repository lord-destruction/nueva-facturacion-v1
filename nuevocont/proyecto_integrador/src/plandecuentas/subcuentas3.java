    private void tabladatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabladatosMouseClicked
        index = tabladatos.getSelectedRow();
        cuenta1 = tabladatos.getValueAt(index, 1).toString();
        codigo = tabladatos.getValueAt(index, 0).toString();
        subcuenta = tabladatos.getValueAt(index, 2).toString();
        if (tablaopciones.getSelectedIndex() == 1) {
            if (subcuenta != null) {
                txtsubcuentan1.setText(cuenta1);
                txtcuentan1.setText(subcuenta);
                txtcuentan1.setEnabled(false);

            }
        } else if (tablaopciones.getSelectedIndex() == 2) {
            if (subcuenta != null) {
                txtsubcuentan2.setText(cuenta1);
                txtcuentan2.setText(subcuenta);
                txtcuentan2.setEnabled(false);
                txtsubcuentan2.setEnabled(false);
            }
        }
    }//GEN-LAST:event_tabladatosMouseClicked

