    private void mostrar() {
        conec.conectar();
        String consulta = "select numero_rol,periodo_rol,empleado_rol,sueldo,(vextras+vsuplementarias)as hextras,bono,aiess,(multas+retenciones+otros+irenta)as retensiones,anticipos,tingresos,tegresos,nrecibir from roles,detalle_rol where roles.codigo_rol=detalle_rol.codigo_rol and detalle_rol.codigo_rol=" + roldepagos.codigo + " and codigo_emprol=" + roldepagos.cedula + "";
        lblnumero.setText(lblnumero.getText() + " " + conec.consultac2get(consulta, "numero_rol"));
        lblperiodo.setText( conec.consultac2get(consulta, "periodo_rol"));
        lblempleado.setText(lblempleado.getText() + " " + conec.consultac2get(consulta, "empleado_rol"));
        lblcargo.setText(lblcargo.getText() + " " + conec.consultac2get("select nombre_cargo from empleados  e,cargos c where e.cod_cargo=c.cod_cargo and e.cedula_empleado="+roldepagos.cedula+"", "nombre_cargo"));
        lblsueldo.setText(lblsueldo.getText() + " " + conec.consultac2get(consulta, "sueldo"));
        lblhoras.setText(lblhoras.getText() + " " + conec.consultac2get(consulta, "hextras"));
        lblcomisiones.setText(lblcomisiones.getText() + " " + conec.consultac2get(consulta, "bono"));
        lblaporte.setText(lblaporte.getText() + " " + conec.consultac2get(consulta, "aiess"));
        lblretensiones.setText(lblretensiones.getText() + " " + conec.consultac2get(consulta, "retensiones"));
        lblprestamos.setText(lblprestamos.getText() + " " + conec.consultac2get(consulta, "anticipos"));
        lbltotali.setText(lbltotali.getText() + " " + conec.consultac2get(consulta, "tingresos"));
        lbltotale.setText(lbltotale.getText() + " " + conec.consultac2get(consulta, "tegresos"));
        lblneto.setText(lblneto.getText() + " " + conec.consultac2get(consulta, "nrecibir"));
        lblci.setText(lblci.getText() + " " + roldepagos.cedula);
        conec.desconectar();
    }

