    private void sumai() {
        double sueldo = Double.parseDouble(tabladatos.getValueAt(index, 3).toString());
        double uno = Double.parseDouble(tabladatos.getValueAt(index, 4).toString());
        double dos = Double.parseDouble(tabladatos.getValueAt(index, 6).toString());
        double tres = Double.parseDouble(tabladatos.getValueAt(index, 8).toString());
        double cuatro = Double.parseDouble(tabladatos.getValueAt(index, 9).toString());
        double suma = sueldo + uno + dos + tres + cuatro;
        tabladatos.setValueAt(conec.redondear(suma), index, 10);
    }

    private void sumae() {
        double uno = Double.parseDouble(tabladatos.getValueAt(index, 11).toString());
        double dos = Double.parseDouble(tabladatos.getValueAt(index, 12).toString());
        double tres = Double.parseDouble(tabladatos.getValueAt(index, 13).toString());
        double cinco = Double.parseDouble(tabladatos.getValueAt(index, 14).toString());
        double seis = Double.parseDouble(tabladatos.getValueAt(index, 15).toString());
        double suma = uno + dos + tres + cinco + seis;
        tabladatos.setValueAt(conec.redondear(suma), index, 16);
    }

    private void totales() {
        double uno = Double.parseDouble(tabladatos.getValueAt(index, 10).toString());
        double dos = Double.parseDouble(tabladatos.getValueAt(index, 16).toString());
        double suma = uno - dos;
        tabladatos.setValueAt(conec.redondear(suma), index, 17);
    }

