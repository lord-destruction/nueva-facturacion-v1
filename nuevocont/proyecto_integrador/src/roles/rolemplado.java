    private void busasig(String cedula) {
        for (int j = 0; j < tabladatos.getRowCount(); j++) {
            if (cedula.equals(tabladatos.getValueAt(j, 2).toString())) {
                tabladatos.setValueAt(txthextras.getText(), j, 5);
                tabladatos.setValueAt(txthsuple.getText(), j, 7);
                tabladatos.setValueAt(txtanticipos.getText(), j, 12);
                tabladatos.setValueAt(txtmultas.getText(), j, 13);
                tabladatos.setValueAt(txtcomisariatos.getText(), j, 14);
            }
        }
    }

