    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        if (txtusuario.getText().length() > 0 && txtclave.getText().length() > 0) {
            conec.conectar();
            String Consulta = "";
            if (chtodos.isSelected()) {
                Consulta = "GRANT all on " + conec.bdget() + ".* TO " + txtusuario.getText() + "@localhost IDENTIFIED BY '" + txtclave.getText() + "';";
            } else {
                for (int j = 0; j < privilegio.length() - 1; j++) {
                    priv = priv + privilegio.substring(j, j + 1);
                }

                Consulta = "GRANT " + priv + " on " + conec.bdget() + ".* TO " + txtusuario.getText() + "@localhost IDENTIFIED BY '" + txtclave.getText() + "';";
            }

            conec.consultaset(Consulta);
            Consulta = "INSERT INTO `alexanderflower`.`privilegios` (`codigo_usuario`, `nombre_usuario`) VALUES ('" + txtcodigo.getText() + "', '" + txtusuario.getText() + "');";
            conec.consultaset(Consulta);
            txtcodigo.setText("");
            txtclave.setText("");
            txtusuario.setText("");
            chtodos.setSelected(false);
            chseleccionar.setSelected(false);
            cheliminar.setSelected(false);
            chmodificar.setSelected(false);
            chinsertar.setSelected(false);
            desactiva();
            txtcodigo.setEnabled(true);
            txtcodigo.requestFocus();
            conec.desconectar();
        }
    }//GEN-LAST:event_btnaceptarActionPerformed
    private void desactiva() {
        txtusuario.setEnabled(false);
        txtclave.setEnabled(false);
        txtclaven.setEnabled(false);
    }

