    private void btnaceptarmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarmActionPerformed
        try {
            if (txtcodigom.getText().length() > 0) {
                conec.conectar();
                if (!"".equals(txtclavem.getText())) {
                    int si = conec.consultac1get("select count(*) from mysql.user where user='" + txtusuariom.getText() + "' and password=password('" + txtclavem.getText() + "')", "count(*)");
                    if (si == 1) {
                        conec.consultaset("UPDATE mysql.user SET Password=PASSWORD('" + txtclavem.getText() + "') WHERE User='" + usuario + "';");
                        conec.consultaset("UPDATE mysql.user SET user='" + txtusuariom.getText() + "' WHERE User='" + usuario + "';");
                        conec.consultaset("UPDATE `alexanderflower`.`privilegios` SET `nombre_usuario`='" + txtusuariom.getText() + "' WHERE `nombre_usuario`='" + usuario + "';");
                    } else {
                        JOptionPane.showMessageDialog(this, "Verifique la informacion");
                    }
                } else {

                    conec.consultaset("UPDATE mysql.user SET user='" + txtusuariom.getText() + "' WHERE User='" + usuario + "';");
                    conec.consultaset("UPDATE `alexanderflower`.`privilegios` SET `nombre_usuario`='" + txtusuariom.getText() + "' WHERE `nombre_usuario`='" + usuario + "';");
                }

            }
            txtcodigom.setText("");
            txtusuariom.setText("");
            txtclavem.setText("");
            txtclaven.setText("");
            limpiar();
            conec.desconectar();
        } catch (RuntimeException ex) {
            System.out.println(""+ex);
        }
    }//GEN-LAST:event_btnaceptarmActionPerformed

    private void txtcodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcodigoKeyPressed
        conec.conectar();
        if (txtcodigo.getText().length() == 10) {
            int ver = conec.consultac1get("select count(*) from empleados where estado_empleado=1 and cedula_empleado like '%" + txtcodigo.getText() + "%'", "count(*)");
            if (ver == 0) {
                nuevoe nu = new nuevoe();
                panel.jd = new JDialog(nu, true);
                panel.jd.setContentPane(nu.getContentPane());
                panel.jd.pack();
                panel.jd.setVisible(true);
            } else {
                ver = conec.consultac1get("select count(*) from privilegios where  codigo_usuario like '%" + txtcodigo.getText() + "%'", "count(*)");
                if (ver == 1) {
                    JOptionPane.showMessageDialog(this, "este codigo contiene ya una cuenta");
                } else {
                    txtcodigo.setEnabled(false);
                    txtusuario.setEnabled(true);
                    txtclave.setEnabled(true);
                }
            }
        }
        if (txtcodigo.getText().length() >= 10) {
            txtcodigo.setText(txtcodigo.getText());
        }
        conec.desconectar();
    }//GEN-LAST:event_txtcodigoKeyPressed

