    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        recorrido();
        conec.conectar();
        conec.consultaset("UPDATE `alexanderflower`.`privilegios` SET `facturacion`=" + f + ", `iventarios`=" + i + ", `clipro`=" + cp + ",`parametros`=" + pa + ", `cuentasxcobrar`=" + cxc + ", `cuentasxpagar`=" + cxp + ", `contabilidad`=" + cont + " WHERE `codigo_usuario`='" + txtcodigo.getText() + "';");
        conec.desconectar();
        this.setVisible(false);
    }//GEN-LAST:event_btnguardarActionPerformed
 private void recorrido() {
        if (cbfa.isSelected()) {
            f = 1;
        }
        if (cbia.isSelected()) {
            i = 1;
        }
        if (cbcpa.isSelected()) {
            cp = 1;
        }
        if (cbpa.isSelected()) {
            pa = 1;
        }
        if (cbcxc.isSelected()) {
            cxc = 1;
        }
        if (cbcxp.isSelected()) {
            cxp = 1;
        }
        if (cbco.isSelected()) {
            cont = 1;
        }

    }
