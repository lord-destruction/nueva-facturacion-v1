    private void tablapanelStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tablapanelStateChanged
        if (tablapanel.getSelectedIndex() == 2) {
            privilegios nu = new privilegios();
            dialogo jd = new dialogo(nu, true);
            jd.setContentPane(nu.getContentPane());
            jd.pack();
            jd.setLocationRelativeTo(null);
            jd.setVisible(true);
        }
        if (tablapanel.getSelectedIndex() == 4) {
            informacionempresas nuevo = new informacionempresas();
            dialogo jd = new dialogo(nuevo, true);
            jd.setContentPane(nuevo.getContentPane());
            jd.pack();
            jd.setLocationRelativeTo(null);
            jd.setVisible(true);
        }
        if (tablapanel.getSelectedIndex() == 1) {
            totalesempresa nuevo = new totalesempresa();
            dialogo jd = new dialogo(nuevo, true);
            jd.setContentPane(nuevo.getContentPane());
            jd.pack();
            jd.setLocationRelativeTo(null);
            jd.setVisible(true);
        }
    }//GEN-LAST:event_tablapanelStateChanged

