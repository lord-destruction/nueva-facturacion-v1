    private void saltocelda() {
        String fila[] = {"", "", "", "", ""};
        DefaultTableModel modelo = (DefaultTableModel) tabladatos.getModel();
        int a = 0;
        for (int i = 1; i < tabladatos.getRowCount(); i++) {
            a++;
            if (("0.0".equals(tabladatos.getValueAt(i - 1, 3).toString()) && !"0.0".equals(tabladatos.getValueAt(i, 3).toString()) && "0.0".equals(tabladatos.getValueAt(i, 4).toString()) && !"0.0".equals(tabladatos.getValueAt(i - 1, 4).toString())) || (!"0.0".equals(tabladatos.getValueAt(i - 1, 3).toString()) && "0.0".equals(tabladatos.getValueAt(i, 3).toString()) && !"0.0".equals(tabladatos.getValueAt(i, 4).toString()) && "0.0".equals(tabladatos.getValueAt(i - 1, 4).toString()))) {
                modelo.insertRow(i + 1, fila);
                i++;
            }
        }
        tabladatos.setModel(modelo);
    }
