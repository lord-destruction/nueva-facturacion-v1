    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed

        if (tabladatos.getRowCount() > 0) {
            int seleccion = JOptionPane.showOptionDialog(this, "¿Confimar eliminacion del asiento", "Eliminar Asiento", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Si", "No"}, "Si");

            if (seleccion == 0) {

                conec.conectar();
                String fact = "";
                if (rdcompras.isSelected()) {
                    fact = conec.consultac2get("select * from transacciones where cod_trans=" + valor + "", "facturaco");
                    conec.consultaset("DELETE FROM transacciones WHERE facturaco=" + fact + ";");
                } else if (rdventas.isSelected()) {
                    fact = conec.consultac2get("select * from transacciones where cod_trans=" + valor + "", "facturave");
                    conec.consultaset("DELETE FROM transacciones WHERE facturave=" + fact + ";");
                }
                if (rdmanual.isSelected()) {
                    fact = conec.consultac2get("select * from transacciones where cod_trans=" + valor + "", "transmanu");
                    conec.consultaset("DELETE FROM transacciones WHERE transmanu=" + fact + ";");
                } else if (rdcxc.isSelected()) {
                    fact = conec.consultac2get("select * from transacciones where cod_trans=" + valor + "", "reciboco");
                    conec.consultaset("DELETE FROM transacciones WHERE reciboco=" + fact + ";");
                }
                if (rdcxp.isSelected()) {
                    fact = conec.consultac2get("select * from transacciones where cod_trans=" + valor + "", "recibove");
                    conec.consultaset("DELETE FROM transacciones WHERE recibove=" + fact + ";");
                } else if (rdrp.isSelected()) {
                    fact = conec.consultac2get("select * from transacciones where cod_trans=" + valor + "", "roles");
                    conec.consultaset("DELETE FROM transacciones WHERE roles=" + fact + ";");
                }

                conec.desconectar();
            }
        } else {
            JOptionPane.showMessageDialog(this, "No existe Asientos");
        }
    }//GEN-LAST:event_btneliminarActionPerformed

