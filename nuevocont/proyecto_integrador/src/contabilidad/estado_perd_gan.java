    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        conec.conectar();
        final String[] columnas = {"Cód Transaccion", "Cód Cuenta", "Descripción", "Ingresos", "Egresos"};
        String consulta = "select t.cod_trans,t.cod_cuenta,t.des_cuenta,t.debe,t.haber from transacciones t ";
        String sr = conec.consultac2get("select sum(transacciones.debe) from transacciones", "sum(transacciones.debe)");
        txtingre.setText(sr);
        String st = conec.consultac2get("select sum(transacciones.haber) from transacciones", "sum(transacciones.haber)");
        txtegre.setText(st);
        String a = conec.consultac2get("select (sum(transacciones.haber)-sum(transacciones.debe))as Egreso from transacciones", "Egreso");
        txtgan.setText(a);
        String b = conec.consultac2get("select (sum(transacciones.haber)-sum(transacciones.debe))as Ingreso from transacciones", "Ingreso");
        txtpier.setText(b);
        tabladatos.setModel(conec.consultaget(consulta, columnas));
        conec.desconectar();
        txtingre.setEditable(false);
        txtegre.setEditable(false);
        txtgan.setEditable(false);
        txtgan.setText("");
        txtgan.setEditable(false);

    }//GEN-LAST:event_jButton1ActionPerformed

