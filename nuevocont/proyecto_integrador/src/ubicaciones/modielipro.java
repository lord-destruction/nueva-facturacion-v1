    private void cargar() {
        imagenes p = new imagenes("/proyecto_integrador/imagenes/fondo_gris.jpg");
        this.add(p);
        this.repaint();

        conec.conectar();
        String consulta = "select * from paises where nombre_pais='" + buscarpro.pais + "'and estado_pais=1";
        cbpais.setModel(conec.consultacget(consulta, "nombre_pais"));

        if (panelparametros.estadobu == 2) {
            lbltitulo.setText("Eliminar Información");
            cbpais.setEnabled(false);
            btnbuscarp.setEnabled(false);
            btneliminar.setLocation(btnactualizar.getLocation().x, btnactualizar.getLocation().x);
            btnactualizar.setVisible(false);
            txtprovincia.setEnabled(false);
        } else if (panelparametros.estadobu == 3) {
            lbltitulo.setText("Acualizar Información");
            btneliminar.setVisible(false);
        }
        codigo = buscarpro.codigo;
        txtprovincia.setText(buscarpro.provincia);
        conec.desconectar();
    }
