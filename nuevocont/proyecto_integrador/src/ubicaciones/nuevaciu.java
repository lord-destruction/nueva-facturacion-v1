    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        if (txtciudad.getText().length() > 0) {
            conec.conectar();
            String consulta = "select * from provincias where provincias.nombre_provincia='" + cbprovincia.getSelectedItem() + "'";
            int indice = conec.consultac1get(consulta, "cod_provincia");
            consulta = "INSERT INTO `alexanderflower`.`ciudades` (`nombre_ciudad`, `cod_provincia`, `estado_ciudad`) VALUES ('" + txtciudad.getText() + "', '" + indice + "','1');";
            conec.consultaset(consulta);
            conec.desconectar();
            txtciudad.setText("");
            txtciudad.requestFocus();
        }
    }//GEN-LAST:event_btnaceptarActionPerformed

