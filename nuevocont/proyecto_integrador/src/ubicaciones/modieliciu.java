    private void cargar() {
        imagenes p = new imagenes("/proyecto_integrador/imagenes/fondo_gris.jpg");
        this.add(p);
        this.repaint();

        conec.conectar();
        String consulta = "select * from paises where nombre_pais='" + buscarciud.pais + "'and estado_pais=1";
        cbpais.setModel(conec.consultacget(consulta, "nombre_pais"));
        consulta = "select * from provincias where nombre_provincia='" + buscarciud.provincia + "' and estado_provincia=1";
        cbprovincia.setModel(conec.consultacget(consulta, "nombre_provincia"));
        conec.desconectar();
        txtciudad.setText(buscarciud.ciudad);
        codigo = buscarciud.codigo;
        if (panelparametros.estadobu == 4) {
            lbltitulo.setText("Eliminar Informaci√≥n");
            cbpais.setEnabled(false);
            cbprovincia.setEnabled(false);
            txtciudad.setEnabled(false);
            btnbuscarpa.setEnabled(false);
            btnbuscarpro.setEnabled(false);
            btneliminar.setLocation(btnactualizar.getLocation().x, btnactualizar.getLocation().y);
            btnactualizar.setVisible(false);
        } else if (panelparametros.estadobu == 5) {
            lbltitulo.setText("Actualizar Informacion");
            btneliminar.setVisible(false);
        }
    }
